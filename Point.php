namespace Goat1000\SVGGraph;

/**
 * Data class for x,y coordinates.
 */
class Point implements \Stringable
{
    public $x = 0;
    public $y = 0;

    public function __construct($x, $y)
    {
        $this->x = $x;
        $this->y = $y;
    }

    public function __toString()
    {
        return new Number($this->x).' '.new Number($this->y);
    }
}
